<template>
  <div v-if="isDataLoaded" class="container mt-10 px-4">
    <div v-if="data.length === 0" class="w-full flex flex-col items-center">
      <h1 class="text-2xl">Looks empty here...</h1>
      <router-link 
        class="          
          mt-6 py-2 px-6 
          rounded-sm
          text-sm text-white 
          bg-at-light-green 
          duration-200 
          border-solid border-2 border-transparent 
          hover:border-at-light-green hover:bg-white hover:text-at-light-green" 
        :to="{ name: 'Create'}"
      >Create Training</router-link>
    </div>
    <div v-else class="grid grid-cols-1 sm:grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-6">
      <router-link 
        class="flex flex-col items-center bg-light-grey p-8 shadow-md cursor-pointer" 
        :to="{ name: 'View-Training', params: { trainingId: training.id } }"
        v-for="(training, idx) in data" :key="idx"
      >
        <img 
          v-if="training.trainingType === 'cardio'" 
          src="@/assets/images/running-light-green.png" 
          class="h-24 w-auto" 
          alt=""
        >
        <img 
          v-else 
          src="../assets/images/dumbbell-light-green.png" 
          class="h-24 w-auto" 
          alt=""
        >
        <p class="mt-6 py-1 px-3 text-xs text-white bg-at-light-green shadow-md rounded-lg">
          {{ training.trainingType }}
        </p>
        <h1 class="mt-8 mb-2 text-center text-xl text-at-light-green">
          {{ training.trainingName }}
        </h1>
      </router-link>
    </div>
  </div>
</template>

<script>
import { ref } from 'vue';
import { supabase } from '../supabase/init';

export default {
  name: "Home",
  components: {},
  setup() {
    const data = ref([])
    const isDataLoaded = ref(false)
    const getData = async () => {
      try {
        const { data: trainings, error } = await supabase.from('trainings').select('*')
        if(error) throw error
        data.value = trainings
        isDataLoaded.value = true
      } catch (error) {
        console.warn(error.message)
      }
    }
    getData()
    return { data, isDataLoaded };
  },
};
</script>
